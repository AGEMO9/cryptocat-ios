#import "NSString+Cryptocat.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation NSString (Cryptocat)

////////////////////////////////////////////////////////////////////////////////////////////////////
- (id)tb_JSONObject {
  NSData *JSONData = [self dataUsingEncoding:NSUTF8StringEncoding];
  return [NSJSONSerialization JSONObjectWithData:JSONData options:0 error:nil];
}

@end
